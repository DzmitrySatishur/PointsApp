namespace PointsApp
{
    class Point
    {
        public long id { get; set; }
        public long coordinateX { get; set; }
        public long coordinateY { get; set; }

        public Point()
        {
            this.id = id;
            this.coordinateX = coordinateX;
            this.coordinateY = coordinateY;
        }
    }
}
