namespace PointsApp
{
    class Point
    {
        public int id { get; set; }
        public int coordinateX { get; set; }
        public int coordinateY { get; set; }

        public Point()
        {
            this.id = id;
            this.coordinateX = coordinateX;
            this.coordinateY = coordinateY;
        }
    }
}
